public class TypeCasting {
    public static void main (String args []){
        float marks = 29.14f ;
        int marks2 = (int) marks ;
        System.out.println(marks2);
        int a = 21 ;
        float b = (float) a ;
        System.out.println(b);
        char q = 'a' ;
        int num = q ;
        System.out.println(num);

    }
}
