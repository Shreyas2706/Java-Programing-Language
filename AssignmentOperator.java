public class AssignmentOperator {
    public static void main (String args []) {
        int a = 10 ;
       // a = a + 10 ;
       a+= 20 ; 
       System.out.println(a);
        int b = 10 ;
       // b = b-2 ;
        b-=2 ; 
        System.out.println(b);
       int c = 100 ; 
     //  c = 100*2 ;
       c*=2 ;
       System.out.println(c);
       int d = 100 ;
      // d = 100/4 ;
      d/=4 ;
       System.out.println(d);
    }
    
}
