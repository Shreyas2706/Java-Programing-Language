public class Swap_Function {
    public static void main(String[] args) {
        // SWAP Exchange value
        int a = 10 ;
        int b = 5 ;

        // swap 
        int temp = a ;
        a = b ;    
        b = temp ;

        System.out.println("a = "+a);
        System.out.println("b = " +b);
        
    }
}
